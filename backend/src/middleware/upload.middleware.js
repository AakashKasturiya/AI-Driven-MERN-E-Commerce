// ==========================================
// Import Packages - START
// ==========================================

import multer from "multer";

// ==========================================
// Import Packages - END
// ==========================================

// ==========================================
// Multer Storage - START
// ==========================================

const storage = multer.memoryStorage();

// ==========================================
// Multer Storage - END
// ==========================================

// ==========================================
// Upload Middleware - START
// ==========================================

const upload = multer({
  storage,
  limits: {
    fileSize: 5 * 1024 * 1024, // 5MB
  },
});

// ==========================================
// Upload Middleware - END
// ==========================================

export default upload;